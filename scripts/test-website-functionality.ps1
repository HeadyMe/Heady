$websites = @(
  "http://localhost:8080",
  "http://heady-manager-headyme.onrender.com",
  "http://heady-manager-headysystems.onrender.com"
)

foreach ($site in $websites) {
  try {
    $response = Invoke-WebRequest -Uri $site -UseBasicParsing
    if ($response.StatusCode -ne 200) {
      throw "HTTP $($response.StatusCode)"
    }
    Write-Host "$site is functional" -ForegroundColor Green
  }
  catch {
    Write-Host "$site is DOWN: $_" -ForegroundColor Red
  }
}
